    private boolean validateInput() {
    username=txtUsername.getText();
     char[] pwd=txtPassword.getPassword();
     char[] repwd=txtPassword1.getPassword();
     
    
     if(username.isEmpty()==true||pwd.length==0||repwd.length==0)
         return false;
     password=String.valueOf(pwd);
     password1=String.valueOf(repwd);
     return true;
    }

    private boolean validatePassword() {
       char[] pwd=txtPassword.getPassword();
     char[] repwd=txtPassword1.getPassword(); 
      password=String.valueOf(pwd);
     password1=String.valueOf(repwd);
       if(pwd.length==repwd.length&& password.equals(password1))
       {
        return true;}
       else
       {
       return false;}
    }

