    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        if(validateInput()==false)
        {
             JOptionPane.showMessageDialog(null, "Username or password cannot be left blank!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if(validatePassword()==false)   
        {
            JOptionPane.showMessageDialog(null, "Please type the password again!", "Error", JOptionPane.ERROR_MESSAGE);
            return; 
        }
       if(validateInput()==true){
            try
            {
            User user=new User(username,password,"Student");
                boolean status = UserDAO.addUser(user);
                if(status == true){
                    JOptionPane.showMessageDialog(null, "Registration Successfully Done");
                    clearAll();
                }
                else
                JOptionPane.showMessageDialog(null,"UserID already present","Registration Denied!",JOptionPane.ERROR_MESSAGE);
            JOptionPane.showMessageDialog(null, "Done!", "success", JOptionPane.INFORMATION_MESSAGE);
           
            }
            catch(SQLException e)
            {
               JOptionPane.showMessageDialog(null, "SQLException in registerframe!", "Error", JOptionPane.ERROR_MESSAGE);
          e.printStackTrace();
            }
       }
    }//GEN-LAST:event_btnRegisterActionPerformed

